// 長さでソートしたエリアコード一覧（最長一致のため降順）
const SORTED_AREA_CODES = Object.keys(AREA_CODE_MAP).sort((a, b) => b.length - a.length);

/**
 * 国内番号（先頭の0を含む）からエリアコードを検索して地域名を返す
 * 最長一致（longest prefix first）で検索する
 * 
 * @param nationalNumber - 国内表記の電話番号（例: "0312345678", "045-123-4567"）
 * @returns 地域名（見つからない場合は null）
 * 
 * @example
 * getRegionName('0312345678')   // => '東京23区'
 * getRegionName('09012345678')  // => null (携帯電話はnull)
 * getRegionName('04512345678')  // => '横浜'
 */
export function getRegionName(nationalNumber: string): string | null {
  // 数字のみに正規化
  const digits = nationalNumber.replace(/\D/g, '');
  
  // 携帯・IP電話・フリーダイヤルはnull
  if (/^(070|080|090|050|0120|0800|0570|0990)/.test(digits)) {
    return null;
  }
  
  // 最長一致で検索
  for (const code of SORTED_AREA_CODES) {
    if (digits.startsWith(code)) {
      return AREA_CODE_MAP[code] ?? null;
    }
  }
  
  return null;
}
